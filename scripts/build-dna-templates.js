#!/usr/bin/env node
/**
 * DNA Bridge: converts Darlkom templates.json → Slide Helper TEMPLATES format.
 * Reads from ../../darlkom-banana/templates.json
 * Outputs dna-templates.js to ../dna-templates.js
 */
const fs = require("fs");
const path = require("path");

const SRC = path.resolve(__dirname, "../../darlkom-banana/templates.json");
const OUT = path.resolve(__dirname, "../dna-templates.js");

const ROLE_TO_CATEGORY = {
  Opening: "오프닝",
  Structure: "구조",
  "High Impact": "임팩트",
  "Content / Data": "데이터",
  Corporate: "비즈니스",
  Creative: "크리에이티브",
  Emotional: "감성",
};

function generatePromptFromDNA(dna) {
  const d = dna.design_dna || {};
  const colors = d.color_palette || {};
  const mood = (d.emotional_profile?.mood || []).join(", ");
  const tones = (d.tone_keywords || []).join(", ");
  const mat = d.materiality?.base || "digital";
  const layout = d.layout_rules?.composition || "balanced";
  const ws = d.layout_rules?.whitespace_ratio
    ? `${Math.round(d.layout_rules.whitespace_ratio * 100)}% whitespace`
    : "";
  const typo = d.typography?.headline || "";

  const parts = [
    `Apply "${dna.style_name}" style to this slide.`,
    tones ? `Tone: ${tones}.` : "",
    mood ? `Mood: ${mood}.` : "",
    `Layout: ${layout}${ws ? `, ${ws}` : ""}.`,
    mat !== "digital_screen" && mat !== "digital"
      ? `Material feel: ${mat}.`
      : "",
    typo && typo !== "sans-serif" ? `Typography: ${typo}.` : "",
    "Keep text readable and visual hierarchy clear.",
  ];

  return parts.filter(Boolean).join(" ");
}

function buildDescription(dna) {
  const d = dna.design_dna || {};
  const mood = (d.emotional_profile?.mood || []).slice(0, 3).join(", ");
  const best = (dna.slide_usage?.best_for || []).slice(0, 2).join(", ");
  const parts = [
    dna.style_name,
    mood ? `— ${mood}` : "",
    best ? `. 추천: ${best}` : "",
  ];
  return parts.filter(Boolean).join("");
}

// --- Main ---
const raw = JSON.parse(fs.readFileSync(SRC, "utf8"));
const allDna = raw.styles_v2 || [];

const templates = allDna
  .filter((dna) => {
    // Must have a valid style name
    if (!dna.style_name || dna.style_name.trim().length < 3) return false;
    return true;
  })
  .map((dna) => {
    const category = ROLE_TO_CATEGORY[dna.role_bucket] || "기타";
    const hasRichPrompt =
      dna.image_prompt_one_line && dna.image_prompt_one_line.trim().length > 10;

    return {
      id: dna.module_id.toLowerCase().replace(/_/g, "-"),
      category,
      title: dna.style_name,
      description: buildDescription(dna),
      prompt: hasRichPrompt
        ? `Apply this visual style to the slide: ${dna.image_prompt_one_line.trim().slice(0, 400)}`
        : generatePromptFromDNA(dna),
      dnaId: dna.module_id,
      roleBucket: dna.role_bucket,
    };
  });

const output = `// Auto-generated by build-dna-templates.js — ${new Date().toISOString().split("T")[0]}
// Source: darlkom-banana/templates.json (${allDna.length} DNAs → ${templates.length} templates)
const DNA_TEMPLATES = ${JSON.stringify(templates, null, 2)};
`;

fs.writeFileSync(OUT, output, "utf8");
console.log(`✅ Generated ${templates.length} DNA templates → ${OUT}`);
console.log(
  `   Categories: ${JSON.stringify(
    Object.fromEntries(
      [...new Set(templates.map((t) => t.category))].map((c) => [
        c,
        templates.filter((t) => t.category === c).length,
      ])
    )
  )}`
);
